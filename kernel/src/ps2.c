char scan_code_to_letter(uint8_t scan_code) {
    uint8_t offset = 93;
    return scan_code + offset;
}

bool has_byte() { return (port_in_u8(0x64) & 1) != 0; }

void Ps2Init() {
    uint8_t r = 14;
    uint8_t c = 10;
    while (1) {
        if (has_byte()) {
            uint8_t scan_code = port_in_u8(0x60);
            if (scan_code & 0x80) continue;

            scan_code = translate_table[scan_code];

            if (scan_code >= 4 && scan_code <= 0x1d) {
                put_char(scan_code_to_letter(scan_code), c++, 14, g_fg_color, g_bg_color);
            }
        }
    }
}